var exp = require('express');
var route = exp.Router();
var sale_payment = require('../model/Sales_Payment_model');

route.get('/:id?', function (req, res, next) {
    if (req.params.id) {

        sale_payment.getSales_PaymentById(req.params.id, function (err, rows) {

            if (err) {
                res.json(err);
            }
            else {
                res.json(rows);
            }
        });
    }
    else {

        sale_payment.getAllSales_Payment(function (err, row) {

            if (err) {
                res.json(err);
            }
            else {
                res.json(row);
            }
        });
    }
});

route.delete('/:id', function (req, res, next) {

    sale_payment.deleteSales_PaymentById(req.params.id, function (err, row) {
        if (err) {
            res.json(err);
        }
        else {
            res.json(row);
        }

    });
});

route.put('/:id', function (req, res, next) {

    sale_payment.updateSales_Payment(req.params.id, req.body, function (err, row) {

        if (err) {
            res.json(err);
        }
        else {
            res.json(row);
        }
    });
});

route.post('/', function (req, res, next) {

    sale_payment.addSales_Payment(req.body, function (err, count) {

        if (err) {
            res.json(err);
        }
        else {
            res.json(req.body);
        }
    });
});

module.exports = route;