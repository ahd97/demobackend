var exp = require('express');
var route = exp.Router();
var purchase_return = require('../model/Purchase_return_model');

route.get('/:id?', function (req, res, next) {
    if (req.params.id) {

        purchase_return.getAllPurchase_Return(req.params.id, function (err, rows) {

            if (err) {
                res.json(err);
            }
            else {
                res.json(rows);
            }
        });
    }
    else {

        purchase_return.getAllPurchase_Return(function (err, row) {

            if (err) {
                res.json(err);
            }
            else {
                res.json(row);
            }
        });
    }
});

route.delete('/:id', function (req, res, next) {

    purchase_return.deletePurchase_ReturnById(req.params.id, function (err, row) {
        if (err) {
            res.json(err);
        }
        else {
            res.json(row);
        }

    });
});

route.put('/:id', function (req, res, next) {

    purchase_return.updatePurchase_Return(req.params.id, req.body, function (err, row) {

        if (err) {
            res.json(err);
        }
        else {
            res.json(row);
        }
    });
});

route.post('/', function (req, res, next) {

    purchase_return.addPurchase_Return(req.body, function (err, count) {

        if (err) {
            res.json(err);
        }
        else {
            res.json(req.body);
        }
    });
});

module.exports = route;